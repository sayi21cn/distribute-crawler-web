package xu.main.java.distribute_crawler_web.vo;

public class MyRulePattern {

	private int id;
	private String identifyCharacters;
	private int maxParseLength;
	private String patternName;
	private String numberToTel;
	private String numberToCommon;
	private String numberToNature;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getIdentifyCharacters() {
		return identifyCharacters;
	}

	public void setIdentifyCharacters(String identifyCharacters) {
		this.identifyCharacters = identifyCharacters;
	}

	public int getMaxParseLength() {
		return maxParseLength;
	}

	public void setMaxParseLength(int maxParseLength) {
		this.maxParseLength = maxParseLength;
	}

	public String getPatternName() {
		return patternName;
	}

	public void setPatternName(String patternName) {
		this.patternName = patternName;
	}

	public String getNumberToTel() {
		return numberToTel;
	}

	public void setNumberToTel(String numberToTel) {
		this.numberToTel = numberToTel;
	}

	public String getNumberToCommon() {
		return numberToCommon;
	}

	public void setNumberToCommon(String numberToCommon) {
		this.numberToCommon = numberToCommon;
	}

	public String getNumberToNature() {
		return numberToNature;
	}

	public void setNumberToNature(String numberToNature) {
		this.numberToNature = numberToNature;
	}

}
